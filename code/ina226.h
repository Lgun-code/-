#ifndef __INA226_H__
#define __INA226_H__

#include "main.h"

/* ==================================================================== */
/* ==================== 用户引脚配置区 (按需修改) ======================= */
/* ==================================================================== */

// SCL 引脚定义
#define INA226_SCL_PORT             GPIOB
#define INA226_SCL_PIN              GPIO_PIN_9
#define INA226_SCL_CLK_ENABLE()     __HAL_RCC_GPIOB_CLK_ENABLE()

// SDA 引脚定义
#define INA226_SDA_PORT             GPIOB
#define INA226_SDA_PIN              GPIO_PIN_8
#define INA226_SDA_CLK_ENABLE()     __HAL_RCC_GPIOB_CLK_ENABLE()

/* ==================================================================== */
/* ==================== INA226 硬件参数配置区 =========================== */
/* ==================================================================== */

// INA226 的 I2C 器件地址 (A0,A1接GND时为0x80; 如果是其他接法需修改)
#define INA226_ADDR                 0x88

// 采样电阻阻值 (单位: 欧姆) 
// 市面常见模块默认多为 0.1欧姆 (R100)
#define INA226_R_SHUNT              0.1f

// 设定的电流分辨率 LSB (单位: mA/bit)
// 0.1mA 的分辨率对于 0.1欧姆 采样电阻非常合理，最大可测 3.27A
#define INA226_CURRENT_LSB          0.1f 

// 校准寄存器计算公式：CAL = 0.00512 / (Current_LSB * R_Shunt)
// 此处自动计算出准确的校准值，无需手动推算十六进制
#define INA226_CAL_VALUE            ((uint16_t)(0.00512f / ((INA226_CURRENT_LSB / 1000.0f) * INA226_R_SHUNT)))

/* ==================================================================== */
/* ==================== 暴露的外部调用函数接口 =========================== */
/* ==================================================================== */

uint8_t INA226_Init(void);
float INA226_Get_Current(void);     // 返回精准电流，单位: mA (带正负号)
float INA226_Get_BusVoltage(void);  // 返回总线电压，单位: V
float INA226_Get_Power(void);       // 返回功率，单位: mW

#endif